# defining types of people as an integer
types_of_people = 10
# defining x as a string with the variable
x = f"There are {types_of_people} types of people."

# defining binary as a string
binary = "binary"
# defining do not as a string
do_not = "don't"
# defining y as a string with 2 variables
y = f"Those who know {binary} and those who {do_not}."

# printing the setup
print(x)
# printing the punchline
print(y)

# printing the setup inside another string
print(f"I said: {x}")
# printing the punchline inside another string
print(f"I also said: {y}")

# defining hilarious as a boolean
hilarious = False
# defining joke evaluation as a string
joke_evaluation = "Isn't that joke so funny?! {}"

# printing joke evaluation while adding hilarious
print(joke_evaluation.format(hilarious))

# defining one string
w = "This is the left side of..."
# defining another string
e = "a string with a right side."

